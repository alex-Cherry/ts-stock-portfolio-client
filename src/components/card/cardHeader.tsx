import React from 'react';


////////////////////////////////////////////////////////////////////////////////
// 
// EXTRA
// 
////////////////////////////////////////////////////////////////////////////////

type CardHeaderProps = {
  // text of the header
  children: string
}


////////////////////////////////////////////////////////////////////////////////
// 
// COMPONENT
// 
////////////////////////////////////////////////////////////////////////////////

const CardHeader = (props: CardHeaderProps) => {

  const { children: headerText } = props;

  // if headerText isn't set, return null
  if (!headerText) {
    return null;
  }


  // RENDER
  return (
    <h3 className="card__header">
      { headerText }
    </h3>
  );
}

export default CardHeader;
